
package eje.primitivosflotantes;

//@author Sistemas

public class EjePrimitivosFlotantes {

    public static void main(String[] args) {
        // Datos primitivos flotantes "DECIMALES"; float, double
        
        //float 
        float numeroFloat = 10.4F;
        
        System.out.println("Numero Flotante; "+numeroFloat);
        System.out.println("valor minimo float; "+ Float.MIN_VALUE);
        System.out.println("valor maximo float; "+ Float.MAX_VALUE);
        
        System.out.println("---------------------------------------------------");
        //doble
        
        double numeroDouble = 34.1;
        System.out.println("Numero Double; "+numeroDouble);
        System.out.println("valor minimo Double; "+ Double.MIN_VALUE);
        System.out.println("valor maximo Double; "+ Double.MAX_VALUE);
    }

}
