
package uso.var.tipoprimitivo;

//@author Sistemas

public class UsoVarTipoPrimitivo {

    public static void main(String[] args) {
        // Uso de la palabra reservada --> var
        
        var numeroEntero = 10;
        System.out.println("Numero entero; "+numeroEntero);
        
        var numeroFloat = 10.0f;
        
        System.out.println("Variable tipo Float: " + numeroFloat);
    }

}
